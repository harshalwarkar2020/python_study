"""Tuples In Python
Tuples are used to store multiple items in a single variable.
Tuples are written with round brackets.
Tuples are ordered, meaning that the items have a defined order, and that order will not change.
Tuples are unchangeable, meaning that we cannot modify items after the tuple has been created.
Tuples are allowed to have duplicate values.
Tuple items are indexed, the first item has index [0], the second item has index [1] etc..
"""


# Create a Tuple
thistuple = ("apple", "banana", "cherry")
print(thistuple)

# Tuple allow duplicate values
thistuple1 = ("apple", "banana", "cherry", "apple", "cherry")
print(thistuple1)
