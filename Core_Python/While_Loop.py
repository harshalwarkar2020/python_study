"""While Loop In Python
With the while loop we can execute a set of statements as long as a condition is true.
"""

# Print i as long as i is less than 8:
i = 4
while i < 8:
    print(i)
    i = i + 1

# Print "Good Morning" as long as count is less than 10:
count = 1
while count < 10:
    print("Good Morning")
    count = count + 1